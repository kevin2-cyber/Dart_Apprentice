void main(List<String> args) {
  print('Hello, Dart Apprentice Reader!');
  // Addition
  print(11 + 1);
  // Subtraction
  print(11 - 1);
  // Multiplication
  print(11 * 2);
  // Division
  print(22 / 2);
  // Decimal numbers
  print(22 / 7);
  // truncating division operator
  print(22 ~/ 7);
  // Euclidean modulo
  print(28 % 10);
  print(((8000 / (5 * 10)) - 32) ~/ (29 % 5));
}
