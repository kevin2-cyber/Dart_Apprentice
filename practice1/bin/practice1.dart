import 'dart:math';


void main(List<String> args) {
  print('Hello, Dart Apprentice Reader!');
  // Addition
  print(11 + 1);
  // Subtraction
  print(11 - 1);
  // Multiplication
  print(11 * 2);
  // Division
  print(22 / 2);
  // Decimal numbers
  print(22 / 7);
  // truncating division operator
  print(22 ~/ 7);
  // Euclidean modulo
  print(28 % 10);
  print(((8000 / (5 * 10)) - 32) ~/ (29 % 5));
  // Maths functions
  print(sin(45 * pi / 180));
  print(cos(135 * pi / 180));
  print(max(sqrt(2), pi / 2));
  print(sin(45));
  print(1 / sqrt(2));
}
